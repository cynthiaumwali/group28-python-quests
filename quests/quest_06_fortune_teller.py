# Quest 6: The Fortune Teller
# Concept: input() - asking the user a question and getting their answer

# input() pauses the program and waits for the user to type a response
name = input("What is your name, adventurer? ")
quest = input("What quest do you seek? ")

# Combine the stored answers into a confirmation message
print(f"Greetings, {name}! Your quest to {quest} has been recorded by the Fortune Teller.")